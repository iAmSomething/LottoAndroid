val keystoreProperties =
    Properties().apply {
        val file = rootProject.file("keystore.properties")
        if (file.exists()) {
            file.inputStream().use { load(it) }
        }
    }

fun signingPropOrEnv(
    propKey: String,
    envKey: String,
): String? = (keystoreProperties.getProperty(propKey) ?: System.getenv(envKey))?.takeIf { it.isNotBlank() }

val releaseStoreFilePath = signingPropOrEnv("storeFile", "LOTTO_RELEASE_STORE_FILE")
val releaseStorePassword = signingPropOrEnv("storePassword", "LOTTO_RELEASE_STORE_PASSWORD")
val releaseKeyAlias = signingPropOrEnv("keyAlias", "LOTTO_RELEASE_KEY_ALIAS")
val releaseKeyPassword = signingPropOrEnv("keyPassword", "LOTTO_RELEASE_KEY_PASSWORD")
val hasReleaseSigning =
    !releaseStoreFilePath.isNullOrBlank() &&
        !releaseStorePassword.isNullOrBlank() &&
        !releaseKeyAlias.isNullOrBlank() &&
        !releaseKeyPassword.isNullOrBlank()

